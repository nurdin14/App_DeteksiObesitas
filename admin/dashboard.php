						<h2><li>Diet Sehat</li></h2>
						<p>Pada dasarnya diet yang sehat adalah tentang mengelola pola makan, termasuk memilih jenis makanan yang sebaiknya dikonsumsi. Diet yang sehat adalah ketika semua nutrisi yang dibutuhkan tubuh bisa terpenuhi.</p>
						<p>Beberapa langkah sederhana yang dapat menjadi panduan:</p>
						<p>
							<ul>
								<li><b>Tentukan target yang sehat tiap minggu.</b></li>
								<p>Target-target kecil dan sederhana bernilai penting sebagai langkah menuju perubahan gaya hidup secara berkelanjutan. Selain itu, menetapkan target realistis secara rutin dan berhasil memenuhinya akan membuat Anda merasa positif. Misalnya, jika Anda suka mengonsumsi gorengan tiap hari, targetkan untuk tidak mengonsumsinya dalam waktu seminggu. Sebagai gantinya, konsumsilah selingan sehat seperti buah kering.</p>

								<li><b>Aktif bergerak.</b></li>
								<p>Sebagai langkah awal, Anda tidak perlu berolahraga secara rutin untuk tetap merasa bugar. Lakukan suatu hal yang Anda sukai agar aktivitas tersebut dapat dilakukan berulang-ulang seperti bermain futsal bersama rekan kerja atau bersepeda bersama keluarga. Menjadwalkan olah tubuh di pagi hari sebelum mulai beraktivitas merupakan cara terbaik bagi Anda yang merasa kesulitan untuk meluangkan waktu juga. Usahakan untuk berolahraga setidaknya 2-3 jam per minggu.</p>

								<li><b>Kurangi makanan kemasan.</b></li>
								<p>Batasi konsumsi makanan dalam kemasan, makanan siap saji, dan makanan yang digoreng. Kelompok makanan jenis ini banyak mengandung kalori, bahan pengawet, dan garam.</p>

								<li><b>Pilih makanan yang diolah dengan cara tertentu.</b></li>
								<p>Lebih baik mengonsumsi makanan yang diproses dengan cara dikukus atau direbus dibandingkan yang digoreng di dalam minyak panas. Makanan yang dikukus atau direbus dalam waktu yang tidak terlalu lama memiliki kandungan nutrisi yang lebih lengkap dibandingkan makanan yang digoreng. Sementara itu, makanan yang digoreng mengandung kalori dan lemak jenuh yang tinggi.</p>

								<li><b>Atur ulang isi piring anda.</b></li>
								<p>Penuhi sepertiga bahan makanan di piring dengan daging tanpa lemak, ikan, atau daging unggas. Cobalah untuk memperbanyak porsi buah-buahan, sayur-sayuran, dan kacang-kacangan. Pilihlah jenis sumber karbohidrat yang sehat seperti beras merah atau kentang. Makan buah yang tidak terlalu manis seperti apel, tomat dan avokad.</p>

								<li><b>Memulai dan melanjutkan kebiasaan-kebiasaan baik.</b></li>
								<p>Kebiasaan-kebiasaan sederhana berikut dapat membawa manfaat besar jika dilakukan secara konsisten dan dalam jangka panjang: </p>

								<p>
									<ul>
										<li>Mengonsumsi air mineral dengan cukup dan teratur.</li>
										<li>Jangan melewatkan makanan utama.</li>
										<li>Perbanyak konsumsi sayur agar Anda merasa kenyang lebih lama.</li>
										<li>Hindari membeli atau menyimpan makanan ringan berkalori tinggi di rumah.</li>
										<li>Disiplinkan waktu olahraga Anda.</li>
										<li>Makanlah dengan tenang sambil duduk di meja makan dan bukan sambil bekerja.</li>
									</ul>
								</p>
